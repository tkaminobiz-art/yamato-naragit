// 物件ステータスの定数定義（型安全性の確保）
export const PROPERTY_STATUSES = {
    AVAILABLE: '販売中',
    RESERVED: '商談中',
    SOLD: '成約済',
    NEW: '新着',
} as const;

export type PropertyStatus = typeof PROPERTY_STATUSES[keyof typeof PROPERTY_STATUSES];

// 建築条件の定数定義
export const CONSTRUCTION_CONDITIONS = {
    WITH_CONDITION: '建築条件付き',
    WITHOUT_CONDITION: '建築条件なし',
} as const;

export type ConstructionCondition = typeof CONSTRUCTION_CONDITIONS[keyof typeof CONSTRUCTION_CONDITIONS];

/**
 * 物件データの型定義
 * 長期メンテナンスを考慮し、必須フィールドを明確化
 */
export interface Property {
    // 基本情報（すべて必須）
    id: string;
    name: string;
    address: string;
    city: string; // 市町村名

    // 価格情報（すべて必須）
    price: string; // 表示用（例: "1,880万円", "成約済み"）
    priceNumeric: number; // 計算・絞り込み用（万円単位、0 = 成約済み）

    // 土地情報（すべて必須）
    landSize: string; // 表示用（例: "110.00㎡ (33.27坪)"）
    landSizeNumeric: number; // 計算・絞り込み用（坪単位）

    // アクセス情報
    station: string | null; // 最寄り駅（バス利用などの場合はnull可）
    walkingMinutes: number | null; // 徒歩分数（バス利用などの場合はnull可）

    // 建築条件（必須、後方互換のためboolean型も維持）
    hasConstructionCondition: boolean;
    constructionCondition?: ConstructionCondition; // 将来的な移行用

    // ステータス（必須、型安全）
    status: PropertyStatus | "Available" | "Sold" | "Reserved" | "New"; // 後方互換のため旧値も許可

    // 表示用
    image: string;
    features: string[];

    // 建物プラン関連（必須化、空配列OK）
    recommendedPlanIds: string[]; // 推奨プランID（building_plans.tsのidを参照）
    maxBuildingSize: number; // 建築可能最大面積（㎡）
    buildingCoverage?: number; // 建ぺい率（%）※今後追加推奨
    floorAreaRatio?: number; // 容積率（%）※今後追加推奨
}

